#include "libft.h"

void	ft_strclr(char *s)
{
	if (s)
		while (*s != '\0')
			*s++ = '\0';
}

void	ft_strdel(char **as)
{
	if (as)
	{
		free(*as);
		*as = NULL;
	}
}
